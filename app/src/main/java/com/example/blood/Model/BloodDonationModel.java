package com.example.blood.Model;

public class BloodDonationModel {
   public String name;
    public String email;
    public String phoneno;
    public String age;
    public String address;
    public String bloodgroup;

    public BloodDonationModel(String name, String email, String phoneno, String age, String address, String bloodgroup) {
        this.name = name;
        this.email = email;
        this.phoneno = phoneno;
        this.age = age;
        this.address = address;
        this.bloodgroup = bloodgroup;
    }


}
