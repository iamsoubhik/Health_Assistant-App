package com.example.blood.Model;

public class OrganDonationModel {
    public String name;
    public String email;
    public  String phoneno;
    public  String age;
    public  String address;
    public  String organName;

    public OrganDonationModel(String name, String email, String phoneno, String age, String address, String organName) {
        this.name = name;
        this.email = email;
        this.phoneno = phoneno;
        this.age = age;
        this.address = address;
        this.organName = organName;
    }
}
